public abstract class Shape {
private int x,y;

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public abstract double calculateArea();
    public abstract double calculateCircumference();

}
